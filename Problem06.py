PI = 3.14159265359
radius = float(input('Enter radius: '))

diameter = 2 * radius
circumference = 2 * PI * radius
area = PI * radius * radius

print("\n Diameter = %.f" %diameter,"units")
print(" Circumference = %.2f" %circumference,"units")
print(" Area = %.f" %area,"sq. units")
